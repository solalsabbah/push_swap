#include "libft.h"

int		ft_strnequ(char const *s1, char const *s2, size_t n)
{
	size_t	i;
	int		p;

	i = 0;
	p = 1;
	if (s1 && s2)
	{
		while ((s1[i] != 0 || s2[i] != 0) && i < n - 1)
		{
			if (s1[i] != s2[i])
				p = 0;
			i++;
		}
	}
	if (p == 1 || n == 0)
		return (1);
	return (0);
}
