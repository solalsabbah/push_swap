#include "libft.h"

size_t		count_after(char const *s)
{
	int		count;
	int		i;

	i = 0;
	count = 0;
	while (s[i] != '\0')
		i++;
	i--;
	while ((s[i] == ' ' || s[i] == '\t' || s[i] == '\n') && i > 0)
	{
		i--;
		count++;
	}
	return (count);
}
